#Mesmo código, so que repetição while

c = 1  #A variavel irá começar pelo número 1
while c < 10: #enquanto a varialvel c for menor do que 10, ele irá continuar a contagem ate 9
    print(c)
    c = c + 1 # a função while ira retornar na variavel c e irá somar + 1 para dar continuidade na contagem.
print('Fim')

